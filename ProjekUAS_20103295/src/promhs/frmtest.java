    private void cmdupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdupdateActionPerformed
        String NID = this.txNID.getText();
        String NAMA = this.txNAMA.getText();
        String TGL = this.txTGL.getText();
        String MATKUL = this.txMK.getSelectedItem().toString();
        String JKEL = "L";
        if(this.txJKP.isSelected()){
            JKEL = "P";
        }
        String SQLUpdate = "UPDATE dsn SET NAMA='"+NAMA+"', MATKUL='"+MATKUL+"', JK='"+JKEL+"', TGLLAHIR='"+TGL+"' WHERE NID='"+NID+"';";
        if(this.UpdateData(SQLUpdate)){
            this.getdata();
            JOptionPane.showMessageDialog(null,"data telah di Update");
        }
    }//GEN-LAST:event_cmdupdateActionPerformed

    private void cmdhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdhapusActionPerformed
        String NID = this.txNID.getText();
        String NAMA = this.txNAMA.getText();

        int opsi = JOptionPane.showConfirmDialog(null, "Yakin Akan Menghapus Data dari "+NAMA+"("+NID+")?","Konfirmasi", JOptionPane.YES_NO_OPTION );
        if(opsi == JOptionPane.YES_OPTION ){
            String SQLDelete = "DELETE FROM mhs WHERE NID = '"+NID+"';";
            if(this.UpdateData(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data Telah Terhapus");
            }
        }
    }//GEN-LAST:event_cmdhapusActionPerformed

