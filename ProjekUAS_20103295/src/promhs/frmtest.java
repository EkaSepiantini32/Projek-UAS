    private void tampildata(String sql){
        try{
            Statement stmt = cnn.koneksi().createStatement();
            this.rs = stmt.executeQuery(sql);
        }catch(Exception e){
            System.out.println("Terjadi kendala pada query");
        }
    }
    
    private void getdata(){
        String sql = " Select NID, NAMA, TGLLAHIR, MATKUL, JK FROM dsn;";
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try{
            this.tampildata(sql);
            while(this.rs.next()){
                Object[] obj = new Object[5];
                obj[0] = this.rs.getString("NID");
                obj[1] = this.rs.getString("NAMA");
                obj[2] = this.rs.getString("TGLLAHIR");
                obj[3] = this.rs.getString("MATKUL");
                String txJK = this.rs.getString("jk");
                if(txJK.equals("L")){
                    obj[4] = "Laki - laki";
                }else{
                    obj[4] = "Perempuan ";
                }
                model.addRow(obj);
            }
        }catch(Exception e){
                
        }
    }
    
        private boolean UpdateData(String SQL){
            boolean hsl = false;
            try{
                Statement stmt = cnn.koneksi().createStatement();
                stmt.executeUpdate(SQL);
                hsl = true;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Terjadi Kendala Pada Instruksi");
            }
        return hsl;
        }

