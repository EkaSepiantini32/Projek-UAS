    private void TdsnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TdsnMouseClicked
        String urlGambarNull = "src/img/gambar.jpg";
        this.txNID.setText( model.getValueAt(Tdsn.getSelectedRow(), 0).toString());
        this.txNAMA.setText( model.getValueAt(Tdsn.getSelectedRow(), 1).toString());
        this.txTGL.setText( model.getValueAt(Tdsn.getSelectedRow(), 2).toString());
        this.txMK.setSelectedItem(model.getValueAt(Tdsn.getSelectedRow(), 3).toString());
        String txJK = model.getValueAt(Tdsn.getSelectedRow(), 4).toString();
        if(txJK.equals("Perempuan")){
            this.txJKP.setSelected(true);
        }else{
            this.txJKL.setSelected(true);
        }
        String urlGambar = "src/img/"+this.txNID.getText() + ".jpg";
        BufferedImage loadImg = img.loadImage(urlGambar);
        if(loadImg == null){
            loadImg = img.loadImage(urlGambarNull);
        }
        ImageIcon imageIcon = new ImageIcon(loadImg);
        this.IMGPHOTO.setIcon(imageIcon);

        this.lbCURD.setText("Edit Data "+this.txNID.getText());
        this.cmdstore.setEnabled(false);
        this.cmdupdate.setEnabled(true);
        this.cmdhapus.setEnabled(true);
    }//GEN-LAST:event_TdsnMouseClicked

